#include "libft.h"

int	ft_putnbr(int n)
{
	char	temp;
	int		count;

	count = 0;
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		count += ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
		count += ft_putnbr(n / 10);
	temp = n % 10 + '0';
	count += ft_putchar(temp);
	return (count);
}
